bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    
    for (uint8_t i = 0; i < RGB_MATRIX_LED_COUNT; i++) {
        if (key_array[i].keycode == keycode) {
            if (record->event.pressed){
                key_array[i].pressed = true;
                key_array[i].timer = timer_read();
            }
        }
    }
    return true;
}

bool rgb_matrix_indicators_advanced_user(uint8_t led_min, uint8_t led_max) {

    for (uint8_t i = 0; i < RGB_MATRIX_LED_COUNT; i++) {
        if (key_array[i].pressed) {
            rgb_matrix_set_color(key_array[i].index, 255, 255, 255);
            if (timer_elapsed(key_array[i].timer) > MAX_DELAY) {
                key_array[i].pressed = false;
            }
        }
    }


    if (host_keyboard_led_state().caps_lock) {
        RGB_MATRIX_INDICATOR_SET_COLOR(49, 255, 255, 255); // assuming caps lock is at led #5
    }
    return false;
}



